$(".kart-1").hover(function(){
  $(".kart-govde-1").css("background-color", "white");
  $(".kart-govde-1 > .list-group > .item").addClass("siyah");
  $(".kart-govde-1 > .list-group > .item").removeClass("beyaz");
  }, function(){
  $(".kart-govde-1").css("background-color", "");
  $(".kart-govde-1 > .item").removeClass("siyah");
  $(".kart-govde-1 > .list-group > .item").addClass("beyaz");

});

